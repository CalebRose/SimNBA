// sortTeamsForConferencePairs arranges an 18-team slice so that each confPair
// occupies positions (i, n-1-i), satisfying the protected H&A pairing in the
// 18-team schedule template. Any team not found in the pairs list is left in
// its original relative order in the remaining slots.
func sortTeamsForConferencePairs(teams []structs.Team, pairs []confPair) []structs.Team {
	// Shuffle pair order so slot assignments vary each season
	rng := rand.New(rand.NewSource(time.Now().UnixNano()))
	shuffledPairs := make([]confPair, len(pairs))
	copy(shuffledPairs, pairs)
	rng.Shuffle(len(shuffledPairs), func(i, j int) {
		shuffledPairs[i], shuffledPairs[j] = shuffledPairs[j], shuffledPairs[i]
	})

	n := len(teams)
	sorted := make([]structs.Team, n)
	used := make(map[uint]bool)

	// Build abbr -> team lookup
	abbrMap := make(map[string]structs.Team)
	for _, t := range teams {
		abbrMap[t.Abbr] = t
	}

	// Place each pair at positions (i, n-1-i)
	for i, pair := range shuffledPairs {
		if team, ok := abbrMap[pair.abbrA]; ok {
			sorted[i] = team
			used[team.ID] = true
		}
		if team, ok := abbrMap[pair.abbrB]; ok {
			sorted[n-1-i] = team
			used[team.ID] = true
		}
	}

	// Fill any remaining zero-value slots with teams not in the pairs list
	remaining := []structs.Team{}
	for _, t := range teams {
		if !used[t.ID] {
			remaining = append(remaining, t)
		}
	}
	ri := 0
	for i := 0; i < n && ri < len(remaining); i++ {
		if sorted[i].ID == 0 {
			sorted[i] = remaining[ri]
			ri++
		}
	}

	return sorted
}

