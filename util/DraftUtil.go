package util

func GetDrafteeSalary(pick, year uint) float64 {
	salary := getSalaryMap()
	if pick >= 1 && pick <= 32 && year >= 1 && year <= 4 {
		return salary[pick-1][year-1]
	}
	if pick > 32 && (year == 1 || year == 2) {
		return 0.9
	}
	return 0
}

func GetYearsRemainingForDrafteeContract(pick uint) uint {
	if pick < 33 {
		return 4
	}
	return 2
}

